namespace Universal_Lift_Structure;

// ============================================================
// 【升降控制模式枚举】
// ============================================================
// 定义升降机控制器的三种工作模式，影响玩家如何操控升降机
//
// 【用途】
// - 此枚举被存储在 UniversalLiftStructureSettings 中作为全局配置
// - 控制器根据此设置决定是否响应玩家的手动操作
//
// 【模式说明】
// Remote（远程模式）：
//   - 玩家可直接点击控制器的 Gizmo 按钮触发升降
//   - 无需小人到场操作
//   - 适合快速操作和大规模管理
//
// Console（控制台模式）：
//   - 需要通过专用的控制台建筑（ULS_LiftConsole）操作
//   - 小人需要走到控制台前才能触发升降
//   - 适合模拟真实操作流程
//
// Manual（手动模式）：
//   - 玩家点击 Gizmo 后会创建工作任务（Job）
//   - 小人需要走到控制器前执行"扳动"动作才能触发升降
//   - 最符合原版 RimWorld 的交互逻辑
// ============================================================
public enum LiftControlMode
{
    // 远程模式
    // 玩家可远程控制，无需小人到场
    Remote = 0,

    // 控制台模式
    // 需要通过控制台建筑操作
    Console = 1,

    // 手动模式
    // 小人需要走到控制器前手动操作
    Manual = 2
}