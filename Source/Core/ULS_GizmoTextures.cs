namespace Universal_Lift_Structure;

// ============================================================
// 【Gizmo 图标纹理缓存】
// ============================================================
// 此类负责在游戏启动时加载和缓存所有 Gizmo 按钮的图标纹理
//
// 【核心职责】
// 为控制器的 Gizmo 按钮提供图标纹理
//
// 【加载时机】
// [StaticConstructorOnStartup] 特性确保在游戏启动时自动加载所有纹理
// 避免运行时加载造成的性能开销
//
// 【纹理来源】
// 1. 复用原版图标：直接使用 RimWorld 原版的 UI 图标
// 2. 自定义图标：使用 Mod 自带的图标文件
//
// 【纹理路径规则】
// ContentFinder<Texture2D>.Get("路径") 会在以下位置查找：
// - 自定义图标：Textures/路径.png
// - 原版图标：RimWorld 核心文件中的 Textures/路径.png
// ============================================================
[StaticConstructorOnStartup]
public static class ULS_GizmoTextures
{
    // ============================================================
    // 【复用原版 RimWorld 图标】
    // ============================================================
    // 这些图标复用 RimWorld 原版的 UI 图标，节省美术资源
    // ============================================================


    // ============================================================
    // 【合并编组图标】
    // ============================================================
    // 复用原版"链接存储设置"图标 (UI/Commands/LinkStorageSettings)
    // 用于 Gizmo 按钮：合并多个控制器到同一分组
    // ============================================================
    public static readonly Texture2D MergeGroups = ContentFinder<Texture2D>.Get("UI/Commands/LinkStorageSettings");


    // ============================================================
    // 【拆分编组图标】
    // ============================================================
    // 复用原版"解除链接存储设置"图标 (UI/Commands/UnlinkStorageSettings)
    // 用于 Gizmo 按钮：从分组中移除当前控制器
    // ============================================================
    public static readonly Texture2D SplitGroup = ContentFinder<Texture2D>.Get("UI/Commands/UnlinkStorageSettings");

    // ============================================================
    // 【自定义 UI 图标】
    // ============================================================
    // 这些图标是 Mod 自带的图标文件
    // 位于 Textures/UI/ 目录下
    // ============================================================


    // ============================================================
    // 【设定 ID 图标】
    // ============================================================
    // 自定义标签/标记图标 (Textures/UI/TagSet.png)
    // 用于 Gizmo 按钮：打开设定分组 ID 的对话框
    // ============================================================
    public static readonly Texture2D SetGroupId = ContentFinder<Texture2D>.Get("UI/TagSet");


    // ============================================================
    // 【设定自动编组过滤图标】
    // ============================================================
    // 自定义扫描/过滤图标 (Textures/UI/ScanSet.png)
    // 用于 Gizmo 按钮：设置自动分组的 Pawn 类型过滤（敌对/友好/野生）
    // ============================================================
    public static readonly Texture2D SetAutoGroupFilter = ContentFinder<Texture2D>.Get("UI/ScanSet");


    // ============================================================
    // 【升起编组图标】
    // ============================================================
    // 自定义向上箭头图标 (Textures/UI/Up.png)
    // 用于 Gizmo 按钮：升起分组内所有控制器存储的建筑
    // ============================================================
    public static readonly Texture2D RaiseGroup = ContentFinder<Texture2D>.Get("UI/Up");


    // ============================================================
    // 【降下编组图标】
    // ============================================================
    // 自定义向下箭头图标 (Textures/UI/Down.png)
    // 用于 Gizmo 按钮：降下并存储分组内指定范围的建筑
    // ============================================================
    public static readonly Texture2D LowerGroup = ContentFinder<Texture2D>.Get("UI/Down");

    // ============================================================
    // 【未来可能添加的其他图标】
    // ============================================================
    // 保留用于未来扩展的图标示例
    // ============================================================

    // 选择编组内所有成员图标（可选）
    public static readonly Texture2D SelectLinked = ContentFinder<Texture2D>.Get("UI/Commands/SelectAllLinked");

    // ============================================================
    // 【反转模式切换图标】
    // ============================================================
    // 占位符图标（后期替换为自定义图标）
    // 用于 Gizmo 按钮：切换自动分组的反转模式
    // ============================================================
    public static readonly Texture2D ToggleInvertedMode = ContentFinder<Texture2D>.Get("UI/LiftMode");
}