namespace Universal_Lift_Structure;

// ============================================================
// 【纹理缓存类】
// ============================================================
// 此类负责在游戏启动时加载和缓存 UI 纹理资源
//
// 【核心职责】
// 缓存覆盖层显示的主开关图标纹理
//
// 【加载时机】
// [StaticConstructorOnStartup] 特性确保静态构造函数在游戏启动时自动执行
// 这样可以避免运行时加载纹理造成的性能开销
//
// 【纹理路径】
// ContentFinder<Texture2D>.Get() 会在以下位置查找纹理：
// - Textures/UI/ULS_OverlayDisplayMasterToggle.png（PNG格式）
// ============================================================

[StaticConstructorOnStartup]
internal static class ULS_Textures
{
    // ============================================================
    // 【静态构造函数】
    // ============================================================
    // 在游戏启动时自动调用，加载所有纹理资源
    // ============================================================
    static ULS_Textures()
    {
        // 加载覆盖层显示的主开关图标
        OverlayDisplayMasterToggle = ContentFinder<Texture2D>.Get("UI/ULS_OverlayDisplayMasterToggle");
    }

    // ============================================================
    // 【纹理属性】
    // ============================================================

    // ============================================================
    // 【覆盖层显示的主开关图标】
    // ============================================================
    // 用于在地图控制栏显示切换覆盖层显示的按钮图标
    // ============================================================
    internal static Texture2D OverlayDisplayMasterToggle { get; }
}