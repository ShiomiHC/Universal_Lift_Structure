namespace Universal_Lift_Structure;

// ============================================================
// 【材质缓存类】
// ============================================================
// 此类负责在游戏启动时创建和缓存渲染材质
//
// 【核心职责】
// 缓存升降井的基础渲染材质
//
// 【加载时机】
// [StaticConstructorOnStartup] 特性确保静态构造函数在游戏启动时自动执行
//
// 【材质池机制】
// MaterialPool.MatFrom() 会从材质池中获取或创建材质
// - 如果材质已存在，复用已有材质（避免重复创建）
// - 如果材质不存在，创建新材质并加入池中
//
// 【Shader 说明】
// ShaderDatabase.Cutout：带透明度裁剪的着色器，适用于有透明区域的纹理
// ============================================================

[StaticConstructorOnStartup]
internal static class ULS_Materials
{
    // ============================================================
    // 【静态构造函数】
    // ============================================================
    // 在游戏启动时自动调用，创建所有材质资源
    // ============================================================
    static ULS_Materials()
    {
        // 创建升降井的基础材质
        // 纹理路径：Textures/Things/Building/Linked/ULS_LiftShaft_Atlas.png
        // Shader：Cutout（带透明度裁剪）
        LiftShaftBase = MaterialPool.MatFrom("Things/Building/Linked/ULS_LiftShaft_Atlas", ShaderDatabase.Cutout);
    }

    // ============================================================
    // 【材质属性】
    // ============================================================

    // ============================================================
    // 【升降井的基础渲染材质】
    // ============================================================
    // 用于渲染升降井的基础图形
    // 使用 Linked 图集以支持连接纹理效果
    // ============================================================
    internal static Material LiftShaftBase { get; }
}
